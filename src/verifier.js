async function go() {
  const blockchainURL = "https://co3-pantheon.di.unito.it:7545";
  const web3 = new Web3(blockchainURL);

  var buffer = fs.readFileSync(__dirname + "/qrcode.png");

  Jimp.read(buffer, function (err, image) {
    if (err) {
      console.error(err);
    }

    var qr = new qrCode();
    qr.callback = async function (err, value) {
      if (err) {
        console.error(err);
      }

      //console.log(value.result);
      let res = value.result;
      const info = res.split("|");
      console.log(info[0]);
      console.log(info[1]);
      console.log(info[2]);

      var signing_address = await web3.eth.personal.ecRecover(
        info[0] + "|" + info[1],
        info[2]
      );
      console.log("Indirizzo che ha firmato:  ", signing_address);

      if (signing_address.toUpperCase() === info[0].toUpperCase()) {
        //console.log("OK CHIEDO AL CONTRATTO");
        let payload = { id: info[1], address: signing_address };
        let res = await checkOwnership(payload);
        if (res.toUpperCase() === signing_address.toUpperCase()) {
          console.log("NFT DI PROPRIETA' VERIFICATO DAL CONTRATTO");
        }
      } else {
        console.log("PROPRIETA' NON VERIFICATA");
      }
    };
    qr.decode(image.bitmap);
  });
}

